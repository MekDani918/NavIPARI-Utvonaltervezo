 -- DROP DATABASE IF EXISTS `ipari`;
CREATE DATABASE IF NOT EXISTS `ipari` CHARACTER SET utf8 COLLATE utf8_hungarian_ci;
USE `ipari`;

 -- CSOMÃ“PONTOK
CREATE TABLE IF NOT EXISTS `ipari`.`nodes` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `POS_X` DOUBLE NOT NULL,
  `POS_Y` DOUBLE NOT NULL,
  `FLOOR` INT(2) NOT NULL,
  `IS_STAIR` TINYINT NULL,
  PRIMARY KEY (`ID`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8
COLLATE = utf8_hungarian_ci;

 -- KAPCSOLATOK
CREATE TABLE IF NOT EXISTS `ipari`.`adjacency` (
  `ID` INT(11) NOT NULL AUTO_INCREMENT,
  `NODE_ID` INT(11) NOT NULL,
  `NEIGHBOUR_NODE_ID` INT(11) NOT NULL,
  PRIMARY KEY (`ID`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8
COLLATE = utf8_hungarian_ci;

 -- TERMEK
CREATE TABLE IF NOT EXISTS `ipari`.`classrooms` (
  `ID` INT(11) NOT NULL AUTO_INCREMENT,
  `NAME` VARCHAR(45) NOT NULL,
  `NODE_ID` INT(11) NOT NULL,
  PRIMARY KEY (`ID`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8
COLLATE = utf8_hungarian_ci;

 -- ALIASOK
 CREATE TABLE IF NOT EXISTS `ipari`.`aliases` (
  `ID` INT(11) NOT NULL AUTO_INCREMENT,
  `ROOM_ID` INT(11) NOT NULL,
  `ALIAS` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`ID`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8
COLLATE = utf8_hungarian_ci;

 -- ADMINOK
 CREATE TABLE IF NOT EXISTS `ipari`.`admins` (
  `ID` INT(11) NOT NULL AUTO_INCREMENT,
  `USERNAME` varchar(100) COLLATE utf8_hungarian_ci NOT NULL,
  `PASSWORD` varchar(65) COLLATE utf8_hungarian_ci NOT NULL,
  PRIMARY KEY (`ID`))
